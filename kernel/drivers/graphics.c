#include "stdint.h"
#include "stdbool.h"

#include "graphics.h"

uint64_t fb_width, fb_height, fb_pitch, fb_char_width, fb_char_height, console_y;
uint32_t *fb_ptr;
uint8_t font_array[];

// Draws pixel at (x, y) with RGB color
void pixel(uint64_t x, uint64_t y, uint64_t color){
	if(x >= fb_width || y >= fb_height) return;
	fb_ptr[y*(fb_pitch >> 2) + x] = color;
}

// Draws 8x16 character starting at pixel (8x, 16y) with RGB color
// If color_background is set to true, then all the pixels which aren't set in the font array will be colored as background_color
// Else, ignore background_color and set it to whatever value you want
void printc(uint32_t x, uint32_t y, char c, uint64_t color, uint64_t background_color, bool color_background){
	x <<= 3;
	y <<= 4;
	for(int current_y = 0; current_y<16; current_y++){
		for(int current_x = 0; current_x<8; current_x++){
			if (((font_array[(c << 4) + current_y]) >> (8-current_x)) & 1){
				pixel(current_x + x, current_y + y, color);
			} else {
				if (color_background)
					pixel(current_x + x, current_y + y, background_color);
			}
		}
	}
}

// Almost the same as the previous function but with strings instead, and newlines exist
// Returns amount of lines printed
uint32_t prints(uint32_t x, uint32_t y, char *str, uint64_t color, uint64_t background_color, bool color_background){
	uint32_t current_x = x;
	uint32_t current_y = y;
	for(int i = 0; str[i]; i++){
		if(str[i] == '\n'){ // newline
			current_x = 0;
			current_y++;
			continue;
		}
		printc(current_x, current_y, str[i], color, background_color, color_background);
		current_x++;
		if(current_x >= fb_char_width){
			current_x = 0;
			current_y++;
		}
	}
	return current_y - y + 1;
}

// Prints a line (Note: it's not a complete console with i/o etc, just there for easier logging)
void println(char *str){
	console_y += prints(0, console_y, str, 0xffffff, 0x000000, true);
}

// Initializes framebuffer global variables (has to be called in _start for the rest to work)
void initFb(uint64_t width, uint64_t height, uint64_t pitch, uint32_t *ptr){
	fb_width = width;
	fb_height = height;
	fb_pitch = pitch;
	fb_ptr = ptr;
	fb_char_width = fb_width >> 3;
	fb_char_height = fb_height >> 4;
}

